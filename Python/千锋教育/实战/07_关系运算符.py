"""
关系运算符：  结果  True、False
> < >= <=  ==  !=

"""

a = 10
b = 25

print(a > b)  # False
print(a < b)  # True

print(a == b)  # False
print(a != b)  # True

x = 'abf'
y = 'abd'

print(x == y)  # False
print(x < y)  # False
