# list  列表
# 应用场景：当获取到了很多数据的时候，我们可以将他们存储到列表中，然后直接使用列表访问
name_list = ['华晨宇','韩東']
print(name_list)

# tuple 元组
age_tuple = (18,19,20,21,22,23,24,25)
print(age_tuple)


# dict  字典
# 应用场景：scrapy框架使用
# 格式：变量的名字 = {key:value,key1:value1,key2:value2}
person = {'name':'远兮','age':18}
print(person)

